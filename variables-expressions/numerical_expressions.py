# Numerical expressions examples
x = 15 
y = 4

print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x // y =", x // y) #floor division
print("x % y =", x % y)
print("x ** y =", x ** y)

# Combining variables and parentheses
result = (x + y) * (x - y)
print("Result of (x + y) * (x - y) =", result)
